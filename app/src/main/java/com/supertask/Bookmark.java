public class Bookmark {

    private String shirt;
    private String pant;

    public Bookmark(String shirt, String pant) {
        this.shirt = shirt;
        this.pant = pant;
    }

    public String getShirt() {
        return shirt;
    }

    public void setShirt(String shirt) {
        this.shirt = shirt;
    }

    public String getPant() {
        return pant;
    }

    public void setPant(String pant) {
        this.pant = pant;
    }

}
